    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        JFileChooser browser = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de texto","txt");
        browser.setFileFilter(filtro);
        int resultado = browser.showSaveDialog(null);
        if(resultado == JFileChooser.APPROVE_OPTION){
            String arch = browser.getSelectedFile().getAbsolutePath()+".txt";
            String nota = taNota.getText();
            String mensaje = "grabado exitoso";
            FileWriter writer = null;
            try {
                writer = new FileWriter(arch);
                BufferedWriter buffer = new BufferedWriter(writer);
                buffer.write(nota);
                buffer.close();
            } catch (IOException ex) {
                mensaje = "error al grabar";
                Logger.getLogger(ventana.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    writer.close();
                    JOptionPane.showMessageDialog(rootPane, mensaje);
                } catch (IOException ex) {
                    Logger.getLogger(ventana.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        switch(combxTipo.getSelectedIndex()){
            case 0: tabPedidos.setValueAt("Pepperoni", crenglon, 0); break;
            case 1: tabPedidos.setValueAt("Hawaiana", crenglon, 0); break;
            case 2: tabPedidos.setValueAt("Mexicana", crenglon, 0); break;
            case 3: tabPedidos.setValueAt("Italiana", crenglon, 0); break;
            case 4: tabPedidos.setValueAt("4 Quesos", crenglon, 0); break;
        }
        if(rbChica.isSelected()) tabPedidos.setValueAt("Chica", crenglon, 1);
        if(rbMediana.isSelected()) tabPedidos.setValueAt("Mediana", crenglon, 1);
        if(rbGrande.isSelected()) tabPedidos.setValueAt("Grande", crenglon, 1);
        if(rbJumbo.isSelected()) tabPedidos.setValueAt("Jumbo", crenglon, 1);        

        if(cbxQueso.isSelected()) tabPedidos.setValueAt("Si", crenglon, 2);
        if(cboxPina.isSelected()) tabPedidos.setValueAt("Si", crenglon, 3);
        if(cbxAceitunas.isSelected()) tabPedidos.setValueAt("Si", crenglon, 4);
        if(cbxChampinones.isSelected()) tabPedidos.setValueAt("Si", crenglon, 5);
        
        crenglon++;
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void tabPedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabPedidosMouseClicked
        int r = tabPedidos.getSelectedRow();
        String tipo = (String) tabPedidos.getValueAt(r, 0);
        String tam = (String) tabPedidos.getValueAt(r, 1);
        String queso = (String) tabPedidos.getValueAt(r, 2);
        String pina = (String) tabPedidos.getValueAt(r, 3);
        String aceit = (String) tabPedidos.getValueAt(r, 4);
        String cham = (String) tabPedidos.getValueAt(r, 5);
        
        taNotaReconstruida.setText("Pizzeria \"EL POLLO\" \n\n");
        taNotaReconstruida.append("Tipo: "+ tipo);
        taNotaReconstruida.append("\nTamaño: "+tam);
        if(queso != null || pina != null || aceit !=null || cham != null)
            taNotaReconstruida.append("\nExtras: ");
        if(queso != null) taNotaReconstruida.append("\n- Queso ");
        if(pina != null) taNotaReconstruida.append("\n- Piña ");
        if(aceit != null) taNotaReconstruida.append("\n- Aceitunas ");
        if(cham != null) taNotaReconstruida.append("\n- Champiñones ");
    }//GEN-LAST:event_tabPedidosMouseClicked

