    public void calcularPrecio(){
        double total = 50;
        if(rbChica.isSelected()) total = total+10;
        if(rbMediana.isSelected()) total = total+20;
        if(rbGrande.isSelected()) total = total+30;
        if(rbJumbo.isSelected()) total = total+40; 
        generarNota();
        switch(combxTipo.getSelectedIndex()){
            case 0: total = total+5; break;
            case 1: total = total+10; break;
            case 2: total = total+25; break;
            case 3: total = total+15; break;
            case 4: total = total+20; break;
        }
        if(cbxQueso.isSelected()) total = total+15;
        if(cboxPina.isSelected()) total = total+15;
        if(cbxChampinones.isSelected()) total = total+20;
        if(cbxAceitunas.isSelected()) total = total+10;
        lblCosto.setText("Costo = "+total);
    }
    
    public void generarNota(){
        taNota.setText("Pizzeria \"EL POLLO\" \n\n");
        switch(combxTipo.getSelectedIndex()){
            case 0: taNota.append("Ha elegido pizza: PEPPERONNI"); break;
            case 1: taNota.append("Ha elegido pizza: HAWAIANA"); break;
            case 2: taNota.append("Ha elegido pizza: MEXICANA"); break;
            case 3: taNota.append("Ha elegido pizza: ITALIANA"); break;
            case 4: taNota.append("Ha elegido pizza: 4 QUESOS"); break;
        }
        
        if(rbChica.isSelected()) taNota.append("\nTamano chico");
        if(rbMediana.isSelected()) taNota.append("\nTamano mediano");
        if(rbGrande.isSelected()) taNota.append("\nTamano grande");
        if(rbJumbo.isSelected()) taNota.append("\nTamano jumbo");
        
        if(cbxQueso.isSelected()) taNota.append("\n- Queso extra");
        if(cboxPina.isSelected()) taNota.append("\n- Piña extra");
        if(cbxChampinones.isSelected()) taNota.append("\n- Champiñones extra");
        if(cbxAceitunas.isSelected()) taNota.append("\n- Aceitunas extra");
        
    }
